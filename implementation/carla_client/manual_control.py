class ManualControlWindow(object):
    def __init__(self, carla_world, map, vehicle, camera, client):

        pygame.init()
        pygame.font.init()

        self.world = carla_world
        self.map = map
        self.hud = HUD(640, 720)
        self.vehicle = vehicle
        self.world.on_tick(self.hud.on_world_tick)
        self.camera = camera
        self.surface = None
        self.client = client

        self.camera.listen(lambda image: self._parse_image(image))

        self.display = pygame.display.set_mode(
            (MANUAL_CONTROL_WINDOW_WIDTH, MANUAL_CONTROL_WINDOW_HEIGHT),
            pygame.HWSURFACE | pygame.DOUBLEBUF)
        self.display.fill((0, 0, 0))
        pygame.display.flip()
        self.controller = KeyboardControl(self, self.vehicle)

    def _parse_image(self, image):
        array = np.frombuffer(image.raw_data, dtype=np.dtype("uint8"))
        array = np.reshape(array, (image.height, image.width, 4))
        array = array[:, :, :3]
        array = array[:, :, ::-1]
        self.surface = pygame.surfarray.make_surface(array.swapaxes(0, 1))

    def tick(self, clock):
        vehicle_control = self.controller.parse_events(self, clock)
        self.hud.tick(self, clock)
        self.render(self.display)
        pygame.event.pump()
        pygame.display.flip()

        return vehicle_control

    def render(self, display):
        if self.surface is not None:
            display.blit(self.surface, (0, 0))
        self.hud.render(display)


