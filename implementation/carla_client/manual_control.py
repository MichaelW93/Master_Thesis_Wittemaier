class KeyboardControl(object):
    """Class that handles keyboard input."""
    def __init__(self, world, vehicle):

        self.vehicle = vehicle
        if isinstance(world.vehicle, carla.Vehicle):
            self._control = carla.VehicleControl()
            self._lights = carla.VehicleLightState.NONE
            world.vehicle.set_light_state(self._lights)
        else:
            raise NotImplementedError("Actor type not supported")
        self._steer_cache = 0.0
        world.hud.notification("Press 'H' or '?' for help.", seconds=4.0)

    def parse_events(self, world: ManualControlWindow, clock: pygame.time.Clock):
        if isinstance(self._control, carla.VehicleControl):
            current_lights = self._lights

        if isinstance(self._control, carla.VehicleControl):
            manual_control_available = self._parse_vehicle_keys(pygame.key.get_pressed(), clock.get_time())
            self._control.reverse = self._control.gear < 0
            # Set automatic control-related vehicle lights
            if self._control.brake:
                current_lights |= carla.VehicleLightState.Brake
            else: # Remove the Brake flag
                current_lights &= ~carla.VehicleLightState.Brake
            if self._control.reverse:
                current_lights |= carla.VehicleLightState.Reverse
            else: # Remove the Reverse flag
                current_lights &= ~carla.VehicleLightState.Reverse
            if current_lights != self._lights: # Change the light state only if necessary
                self._lights = current_lights
                world.vehicle.set_light_state(carla.VehicleLightState(self._lights))

            if manual_control_available:
                return self._control
            else:
                return None
        else:
            return None

    def _parse_vehicle_keys(self, keys, milliseconds):
        self._control = self.vehicle.get_control()
        #self._steer_cache = self._control.steer
        key_pressed = False
        if keys[K_UP] or keys[K_w]:
            self._control.throttle = min(self._control.throttle + 0.01, 1)
            key_pressed = True

        if keys[K_DOWN] or keys[K_s]:
            self._control.brake = min(self._control.brake + 0.2, 1)
            key_pressed = True

        steer_increment = 5e-4 * milliseconds
        if keys[K_LEFT] or keys[K_a]:
            if self._steer_cache > 0:
                self._steer_cache = 0
            else:
                self._steer_cache -= steer_increment
            key_pressed = True
        elif keys[K_RIGHT] or keys[K_d]:
            if self._steer_cache < 0:
                self._steer_cache = 0
            else:
                self._steer_cache += steer_increment
            key_pressed = True

        self._steer_cache = min(0.7, max(-0.7, self._steer_cache))
        self._control.steer = round(self._steer_cache, 1)

        if not key_pressed:
            self._steer_cache = 0.0

        return key_pressed


